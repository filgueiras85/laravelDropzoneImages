    public function destroy(Image $image)
    {
        if (Gate::denies('delete_image', $image)){
            abort(401);
        }
        if (Storage::delete('public/images/'.$image->path)){
            $image->delete();
            return back()->with('success' , 'image wad delete successfully');
        }else{
            return back()->with('error' , 'error while delete image');
        }
    }

